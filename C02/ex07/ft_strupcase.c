char	*ft_strupcase(char	*str)
{
	int	i;
	int	j;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122)
		{
			j = str[i] - 32;
			str[i] = j;
		}
		i++;
	}
	return (str);
}
