int	ft_iterative_factorial(int nb)
{
	int	i;
	int	f;

	if (nb < 0)
	{
		return (0);
	}
	i = 1;
	f = 1;
	while (i < nb + 1)
	{
		f = f * i++;
	}
	return (f);
}
