int	ft_sqrt(int nb)
{
	long	i;

	i = 0;
	if (nb < 0)
	{
		return (0);
	}
	while (i * i <= nb)
	{
		if (nb == i * i)
		{
			return (i);
		}
		i++;
	}
	return (0);
}
